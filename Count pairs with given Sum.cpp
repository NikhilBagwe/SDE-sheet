int main()
{
    int a[] = {1, 5, 7, 1};
    int n = 4, k = 6;
    int count = 0;
    
    for(int i=0; i<n; i++){
        int sum = 0;
        for(int j=i+1; j<n; j++){
            sum = a[i] + a[j];
            if(sum == k){
                count++;
            }
        }
    }
    
    cout<<count;

    return 0;
}

